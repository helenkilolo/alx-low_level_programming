#include <stdlib.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - modulo two integers
 * @a: first int
 * @b: second int
 * return: modulo result
 */
int mod(int a, int b)
{
	return (a % b);
}
